class ProgressRatioEmbedding(nn.Module):
    """
    This module return rate positional embeddings.
    """
    def __init__(self, 
                 embedding_dim: int, 
                 gaussian_noise: bool= True):
        super().__init__()
        self.embedding_dim = embedding_dim
        self.gaussian_noise = gaussian_noise
        self.lambda_noise = (2 / self.embedding_dim)

    @torch.no_grad()     
    def _sinusoidal_weight(self,rates:torch.Tensor) -> torch.Tensor:
        """Return padded tensor of sinusoidal rate embeddings 
        of shape (B, max_len, embedding_dim)."""

        rates = rates.unsqueeze(-1) #(Batch_size, max_len, 1)
        indices = torch.arange(self.embedding_dim, device=rates.device).unsqueeze(0).unsqueeze(0) //2 #(1, 1, d_model) 

        rpe = rates * indices  # (Batch_size, max_len, d_model)

        rpe[:,:,0::2] = torch.cos(rpe[:,:,0::2])
        rpe[:,:,1::2] = torch.sin(rpe[:,:,1::2])

        return rpe

    def forward(self,
                target_len:torch.LongTensor,
                max_len: Optional[int] = None)-> torch.Tensor:
        
        if max_len is None:
            max_len = target_len.max().item()

        rates = torch.stack([torch.cat([torch.linspace(0,1,l),torch.ones((max_len - l))]) for l in target_len],dim=0).to(target_len.device) # Rates (B, max_len)

        if self.gaussian_noise:
            noise = self.lambda_noise * torch.randn_like(rates,device=target_len.device)
            rates = torch.clamp(rates + noise,0,1) 

        return self._sinusoidal_weight(rates)

